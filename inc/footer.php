<script>
    $(document).ready(function() {
        // Delegate click event to dynamically created elements
        $(document).on("click", ".price-content .button", function(event) {
            event.preventDefault(); // Prevent the default action of the anchor tag

            $("#popup-form").show(); // Display the popup form
        });

        // Close the popup form when the close button is clicked
        $(".close").click(function() {
            $("#popup-form").hide();
        });

        // Close the popup form when the user clicks outside of it
        $(window).click(function(event) {
            if (event.target == $("#popup-form")[0]) {
                $("#popup-form").hide();
            }
        });


    });
    // Handle form submission
    $("#submitButton1").click(function() {
        var name = $('#name1').val();
        var mobile = $('#mobile1').val();
        var email = $('#email1').val();
        var plan = $('#plan').val();
        // var recaptchaResponse = grecaptcha.getResponse();

        var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        var mobileRegex = /^\d{10}$/;

        if (name == '') {
            showError('Please Enter Your Name.');
        } else if (mobile == '') {
            showError('Please Enter Your Mobile Number.');
        } else if (!mobileRegex.test(mobile)) {
            showError('Please enter a 10-digit mobile number.');
            $('#mobile').val('');
            return;
        } else if (email == '') {
            showError('Please Enter Your Email ID.');
        } else if (!emailRegex.test(email)) {
            showError('Please enter a valid email address.');
            return;
        } else if (plan == '') {
            showError('Please Select a Plan.');
        }
        // else if (recaptchaResponse === "") {
        //     showError('Please complete the reCAPTCHA.');
        // } 
        else {
            // Send data to the server using Ajax
            $.ajax({
                type: "POST",
                url: "../price_enquiry.php",
                data: {
                    name: name,
                    email: email,
                    mobile: mobile,
                    plan: plan,
                    // 'g-recaptcha-response': recaptchaResponse
                },
                dataType: "json",
                success: function(response) {
                    if (response.status === "success") {
                        console.log("Form submitted successfully");
                        Swal.fire({
                            icon: 'success',
                            title: 'We have received your inquiry!',
                            text: response.message,
                        });
                        $('#contactForm1')[0].reset();
                        $("#popup-form").hide();
                    } else {
                        showError(response.message);
                    }
                },
                error: function() {
                    showError('An error occurred while processing your request.');
                }
            });
        }
    });

    function showError(message) {
        Swal.fire({
            icon: 'error',
            title: 'Error',
            text: message,
        });
    }
</script>